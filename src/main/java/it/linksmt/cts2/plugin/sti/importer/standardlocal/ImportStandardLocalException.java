package it.linksmt.cts2.plugin.sti.importer.standardlocal;

public class ImportStandardLocalException extends Exception {

	private static final long serialVersionUID = 510786637706919223L;

	public ImportStandardLocalException() {
		super();
	}

	public ImportStandardLocalException(String message, Throwable cause,
			boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public ImportStandardLocalException(String message, Throwable cause) {
		super(message, cause);
	}

	public ImportStandardLocalException(String message) {
		super(message);
	}

	public ImportStandardLocalException(Throwable cause) {
		super(cause);
	}
	
}
